    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed
        ControleUsuarios controleUsuarios = new ControleUsuarios();
        int verificaConc = 0;
        try {
            verificaConc = controleUsuarios.concederLogin(txtLogin.getText(), txtSenha.getText());
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (verificaConc == 0) {
            TelaInicial t1 = null;
            try {
                t1 = new TelaInicial(txtLogin.getText());
            } catch (SQLException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
            t1.setVisible(true);
            dispose();
        } else {
            cnfCadastro.setText(motivoNegacao(verificaConc));
        }
    }//GEN-LAST:event_btnLogarActionPerformed
    public String motivoNegacao(int motivo) {
        if (motivo == 1) {
            return "Campo Login obrigatorio";
        } else if (motivo == 2) {
            return "Campo Senha obrigatorio";
        } else if (motivo == 3) {
            return "Este login n√£o existe";
        } else if (motivo == 4) {
            return "Senha incorreta";
        } else {
            return null;
        }
    }
    
